package com.excel.read.data.analytics;

import java.util.List;

public class DataTuples {

	private String dataTupleName;
	private List<String> tuples;
	public String getDataTupleName() {
		return dataTupleName;
	}
	public void setDataTupleName(String dataTupleName) {
		this.dataTupleName = dataTupleName;
	}
	public List<String> getTuples() {
		return tuples;
	}
	public void setTuples(List<String> tuples) {
		this.tuples = tuples;
	}

}
